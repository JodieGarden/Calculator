public class Calculator {


        public Calculator() {

        }





        public static int add(int num1, int num2){
            return num1+num2;
        }

        public static int subtract(int num1, int num2){
            return num1 - num2;
        }

        public static int multiply(int num1, int num2){
            return num1 * num2;
        }

        public static int divide(int num1, int num2){
            return num1/num2;
        }
    }

